class Profile {
  final String name;
  final String gender;
  final int age;
  final double height;
  final double weight;
  final String bloodType;
  final String phone;
  final String email;
  final String address;

  Profile({
    required this.name,
    required this.gender,
    required this.age,
    required this.height,
    required this.weight,
    required this.bloodType,
    required this.phone,
    required this.email,
    required this.address,
  });
}
